export function generateStory(input: StoryInput): GeneratedStory {
  const { question, context, child } = input;
  const childName = getChildName(child);
  const toy = pickToy(child);
  const hero = getHero(child);
  const category = categorize(question);

  const builder = templates[category] || templates.general;
  const { title, content } = builder(
    childName,
    toy.nickname,
    toy.type,
    hero.name,
    hero.emoji,
    question,
    context
  );

  return {
    title,
    content,
    imageUrl: getImageUrl(category),
  };
}
